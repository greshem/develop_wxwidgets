sub get_include_line($)
{
	(my $class)=@_;
	#$class="wxFile";
	my $header= $class_to_header{$class};
	my $ret_line;
	if(defined($header))
	{
		#print $header."\n";
		$header=~s/\.\///;
		$ret_line="#include <wx/".$header.">\n";
	}
	return $ret_line;
}
